    public void setEmployeeName(String id){
        Connection conn =null;
        Statement st = null;
        ResultSet rs = null;
        String fname = new String();
        String lname = new String();
        try{
            conn = DriverManager.getConnection("jdbc:sqlserver://localhost;databaseName=ClothingMgmtSvr;user=sa;password=sa");
            String sql = "Select firstname, lastname from employee ";
            if(EmployeeCode.getText().length()>0){
                sql = sql + "where e# = '"+id+"'";
            }
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            if(rs.isBeforeFirst()==false){
                JOptionPane.showMessageDialog(this, "There's no employee with the id: "+id);
                return;
            } else {
                while(rs.next()){
                    fname = rs.getString("firstname");
                    lname = rs.getString("lastname");
                    EmployeeName.setText(fname+" "+lname);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Something is wrong!");
            
        } finally{
            try {
                if (conn != null) {
                    conn.close();
                }
                if (st != null) {
                    st.close();
                }
                if (rs != null) {
                    rs.close();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }

        if((row<0)||(row>tbl.getRowCount())){
            JOptionPane.showMessageDialog(this, "Select a row to remove.");
            return;
        } else{
            sum -= (float)tbl.getValueAt(row, 5);
            tbl.removeRow(row);
            Totals.setText(String.valueOf(sum));
        }

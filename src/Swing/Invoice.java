        String e_id = EmployeeCode.getText();
        String c_id = CustomerID.getText();
        String p_id = ProductID.getText();
        String p_name = ProductName.getText();
        int dscnt =  (int) jSpinner2.getValue();
        int qty = (int) jSpinner1.getValue();
        float p_each = Float.parseFloat(ProductPrice.getText());
        float price = Float.parseFloat(ProductPrice.getText())*qty*((100-(float)dscnt)/100);
